"""
config.py — 物理参数 & 全局配置（v5 声-流-固耦合完整版）
================================================================
【v5 升级说明】
  1. 网络输出扩展为 (u, v, p)：x方向声流速度、y方向声流速度、时均压力场
  2. 新增固体力学参数：密度 rho_s、弹性模量 E_s、泊松比 nu_s
  3. 新增声辐射力 (ARF) 相关参数：声压幅值 P0、声速 c_sound
  4. 新增微结构动力学参数：固体质量 m_solid、等效半径 R_eff
  5. 新增固体表面离散化参数：n_surface_pts（表面积分点数）
  6. 保留原有几何域、流体参数、训练工况配置

物理建模思想（时间平均声流代理模型）：
  超声频率 ~ O(100 kHz)，固体在声场中产生高频振动（周期 ~ O(10 μs)），
  但声流驱动的平均运动时间尺度 ~ O(1 s) 远大于振动时间尺度。
  因此本模型不直接学习高频振动轨迹，而是学习时间平均后的声流场 (u, v, p)，
  再通过计算固体表面受力来预测其低频运动行为。

  完整流程：声场参数 → 声流场预测 → 固体受力计算 → 微结构运动预测
"""

# ═══════════════════════════════════════════════════════════════════
# 第零部分：导入依赖库
# ═══════════════════════════════════════════════════════════════════
import math                          # 数学函数库（tan, radians 等）
import numpy as np                   # 数值计算核心库
import torch                         # PyTorch 深度学习框架

# ═══════════════════════════════════════════════════════════════════
# 第一部分：随机种子（保证实验可复现）
# ═══════════════════════════════════════════════════════════════════
torch.manual_seed(42)                # PyTorch 随机种子
np.random.seed(42)                   # NumPy 随机种子

# ═══════════════════════════════════════════════════════════════════
# 第二部分：计算设备自动选择
# ═══════════════════════════════════════════════════════════════════
device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
print(f"[config] 使用设备: {device}")   # 显示当前运行设备（GPU 或 CPU）


# ═══════════════════════════════════════════════════════════════════
# 第三部分：物理参数类（核心配置）
# ═══════════════════════════════════════════════════════════════════
class PhysicsParams:
    """
    封装所有物理参数、几何参数和训练配置。
    参数值参考 COMSOL 仿真设定与 TBME 论文框架。
    """

    # ── 流体域几何参数 [单位: m] ──────────────────────────
    Lx_min = -500e-6                 # x 轴左边界：-500 μm（SI 单位）
    Lx_max =  500e-6                 # x 轴右边界：+500 μm
    Lx     = 1000e-6                 # 流体域总长度：1000 μm = 1 mm
    Ly     =  600e-6                 # 流体域总高度：600 μm

    # ── 等腰三角形固体结构参数 [单位: m] ──────────────────
    # 三角形描述：底边水平，顶点朝 +y 方向（尖端向上）
    # 底边中心位于 (tri_cx, tri_base_y)
    tri_apex_angle = 15.0            # 顶角角度 [度]（锐边结构，产生强声流涡）
    tri_height     = 1e-4            # 三角形高度：100 μm
    # 由顶角和高度推算底边宽度：base = 2 * h * tan(apex/2)
    tri_base = 2 * tri_height * math.tan(math.radians(tri_apex_angle / 2))
    tri_cx     = 0.0                 # 底边中心 x 坐标 [m]（域中心）
    tri_base_y = 0.0                 # 底边所在 y 坐标 [m]（域底部）

    # ── 流体参数（水，25°C）──────────────────────────────
    rho_f   = 1000.0                 # 流体密度 [kg/m³]
    mu      = 1e-3                   # 动力粘度 [Pa·s]（水的典型值）
    nu      = mu / rho_f             # 运动粘度 ν = μ/ρ [m²/s]
    c_sound = 1500.0                 # 声速 [m/s]（水中声速）

    # ── 固体力学参数（PDMS 微结构）────────────────────────
    rho_s  = 1050.0                  # 固体密度 [kg/m³]（PDMS 典型值）
    E_s    = 1.6e9                   # 弹性模量 [Pa]（PDMS，约 1.6 GPa）
    nu_s   = 0.4                     # 泊松比（PDMS 近不可压缩）

    # ── 微结构动力学参数 ──────────────────────────────────
    # 等效质量：用三角形面积 × 密度（二维模型，单位厚度）
    solid_area  = 0.5 * tri_base * tri_height        # 三角形面积 [m²]
    m_solid     = rho_s * solid_area * 1.0            # 质量 [kg]（假设单位厚度 1m）
    R_eff       = math.sqrt(solid_area / math.pi)     # 等效圆半径（用于 Stokes 阻力估算）
    drag_coeff  = 6 * math.pi * mu * R_eff            # Stokes 阻力系数 [N·s/m]

    # ── 声场参数 ──────────────────────────────────────────
    P0_fixed = 1e6                   # 固定声压幅值 [Pa]（1 MPa，COMSOL 设定）

    # ── 固体表面离散化 ────────────────────────────────────
    n_surface_pts = 100              # 三角形表面积分点数（每条边等分）

    # ── 训练工况：仅保留频率参数 freq [Hz] ────────────────
    # 每个元素对应一个 COMSOL 导出的 CSV 数据文件
    cases_train = [
        (1e5,),                      # 0.10 MHz
        (2e5,),                      # 0.20 MHz
        (3e5,),                      # 0.30 MHz
        (4e5,),                      # 0.40 MHz
        (5e5,),                      # 0.50 MHz
        (6e5,),                      # 0.60 MHz
        (7e5,),                      # 0.70 MHz
        (8e5,),                      # 0.80 MHz
        (9e5,),                      # 0.90 MHz
        (1e6,),                      # 1.00 MHz
    ]

    # 测试工况（不参与训练，用于评估模型泛化能力）
    cases_test = [
        (5.5e5,),                    # 0.55 MHz（插值频率）
        (1.05e6,),                   # 1.05 MHz（轻微外推）
    ]

    # ── 归一化参考值 ──────────────────────────────────────
    freq_ref = 1e6                   # 频率归一化参考值 [Hz]（使输入在 [0.1, 1.0] 范围）

    # ── 网络输出定义 ──────────────────────────────────────
    # v5 版本输出 3 个物理量：(u, v, p)
    # u: x方向时均声流速度 [m/s]
    # v: y方向时均声流速度 [m/s]
    # p: 时均压力场 [Pa]
    output_names = ['u', 'v', 'p']   # 输出字段名称列表
    n_outputs    = 3                 # 输出维度

    # ── 动力学仿真参数 ────────────────────────────────────
    dt_dynamics    = 1e-4            # 时间步长 [s]（牛顿积分步长）
    t_total        = 0.1             # 总仿真时间 [s]
    n_steps        = int(t_total / dt_dynamics)  # 时间步数


# ═══════════════════════════════════════════════════════════════════
# 第四部分：全局实例 & 数据路径
# ═══════════════════════════════════════════════════════════════════
params = PhysicsParams()             # 创建全局参数实例

# COMSOL CSV 数据目录（训练/测试文件存放路径）
COMSOL_DATA_DIR = "./data"

# ═══════════════════════════════════════════════════════════════════
# 第五部分：配置信息打印（调试用）
# ═══════════════════════════════════════════════════════════════════
if __name__ == "__main__":
    print(f"流体域: [{params.Lx_min*1e6:.0f}, {params.Lx_max*1e6:.0f}] × [0, {params.Ly*1e6:.0f}] μm")
    print(f"三角形: 底{params.tri_base*1e6:.2f}μm × 高{params.tri_height*1e6:.0f}μm, 顶角{params.tri_apex_angle}°")
    print(f"固体质量: {params.m_solid:.4e} kg, 等效半径: {params.R_eff*1e6:.2f} μm")
    print(f"Stokes 阻力系数: {params.drag_coeff:.4e} N·s/m")
    print(f"训练工况数: {len(params.cases_train)}, 测试工况数: {len(params.cases_test)}")
    print(f"动力学: dt={params.dt_dynamics:.1e}s, T={params.t_total}s, N={params.n_steps}")
    print("[config v5] 自检通过 ✅")
